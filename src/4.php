<?php

// Function to calculate the sum of two numbers
function sum($a, $b) {
    return $a + $b;
}

// Function to calculate the difference of two numbers
function diff($a, $b) {
    return $a - $b;
}

// Function to calculate the product of two numbers
function prod($a, $b) {
    return $a * $b;
}

// Function to calculate the quotient of two numbers
function quot($a, $b) {
    return $a / $b;
}

// Function to calculate the modulus of two numbers
function mod($a, $b) {
    return $a % $b;
}

// Function to check if a number is even or not
function isEven($n) {
    return ($n % 2 == 0);
}

// Function to check if a number is odd or not
function isOdd($n) {
    return ($n % 2 != 0);
}

?>